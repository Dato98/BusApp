package com.example.busapp.services;

public class BusStop {
    private String Name;
    private String StopId;


    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getStopId() {
        return StopId;
    }

    public void setStopId(String stopId) {
        StopId = stopId;
    }
}
